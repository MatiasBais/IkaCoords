  <h1>Ikariam</h1>
<p>
Ikariam is a browser based massively multiplayer online game of the strategy genre produced and maintained by Gameforge AG. The game is set in the era of classical Greece in an archipelago, with players being made ruler of a small town, which they must expand and lead. Ikariam has received generally positive reviews.
</p>

<h2>Gameplay</h2>
<p>
Ikariam has been compared to the Civilization series created by Sid Meier. It has also been compared to the critically acclaimed Travian series created by Travian Games GmbH. The aim of the game is to expand the town which is controlled by the player, to gather resources and conquer or placate other players. The game can be played for short periods of time; building and researching takes place in real time so the player can return at a later point and assign the town's inhabitants new tasks.

Upon registering, players are given control of a small town. The town's inhabitants pay taxes to the player or can be sent to gather resources instead. The game world is split into islands, each of which can support up to 16(or up to 17 if the player purchases ambrosia) players and their towns. Each island has its own saw mill for producing wood and one of the other buildings which produces a luxury resource. There are five main types of resources: Wood, Wine, Marble, Crystal Glass and Sulphur. They are available from the Sawmill, Vineyard, Quarry, Crystal Mine and Sulphur Pit respectively. All these resources are required to progress in the game, making resource acquisition through conquest or trading mandatory. If a certain resource is lacking locally the player may trade with other players to acquire it. Each island's inhabitants may cooperate and donate building materials to improve the efficiency of their island's resource gathering facilities, which enables all local players to obtain resources more quickly. Failure to do so may result in hostilities between island inhabitants. Players can communicate with island mates by using the agora, a message board found on every island that can only be viewed by people with towns on that island.

With resources the player may construct buildings such as barracks, shipyards and academies. Citizens of the town can then be trained as soldiers, sent to research new technologies, or perform other tasks, which they are paid tax money for undertaking. Citizens must be kept happy to prevent them from leaving the town.

The satisfaction of the people is determined by a number of factors. The player can build a tavern that increases happiness in the town by 12 per tavern level. The player can also serve wine in the tavern. This increases happiness by 60 however consumes wine on an hourly basis. In addition, the player can build a museum after researching cultural exchange. Happiness in the town is increased by 20 for each level of the museum. Also, for each level of the museum the player may sign one additional cultural assets treaty with another player. This increases the happiness in town by 50. The player may have no more than one cultural assets treaty with a player. Cultural goods can be moved from one town to another at any time. Happiness can also be improved with certain research and governments.

The game also provides four advisors to guide the player throughout the game; they are shown in the top right of the screen. From left to right the names are: Towns, Military, Research and Diplomacy.

The towns adviser provides the players with updates on the player's empire. He notifies the player In addition, the town adviser lets the player manage trade routes, a paid feature that allows the player to ship resources from one of the player's towns to another town regularly.

The military adviser allows the player to see all the fleets that the player has on the seas and cancel actions these fleets are conducting. The player can also view combat reports that provide overviews of past and ongoing land and naval battles here.

The research adviser lets the player conduct research if the player has enough research points. Research is needed to progress in the game. This adviser also show the player how many research points are currently available, how many scientists are currently working in academies, how many research points are produced hourly, and how long it will be until the player can research certain technologies.

The diplomatic adviser lets the player read and reply to messages the player has received from other players, send messages to players in the player's alliance, view treaties the player has signed, see which agoras on the player's islands have unread posts, and access agoras.
</p>
